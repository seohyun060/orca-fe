export async function getInsights() {
	return fetch('/api/insights', {
		method: 'GET',
	})
		.then((res) => res.json())

		.then((data) => {
			console.log(data);

			return data;
		});
}
export async function getInsightDetail(id) {
	return fetch(`/api/insights/${id}`, {
		method: 'GET',
	})
		.then((res) => res.json())

		.then((data) => {
			console.log(data);

			return data;
		});
}
